document.writeln("Ingresa un número");
var opcion = prompt("Un numero del 1 al 3");
switch (opcion) {
    case "1":
        document.writeln("Opcion 1");
        break;
    case "2":
        document.writeln("Opcion 2");
        break;
    default:
        document.writeln("Valor default")
        break;
    case "3":
        document.writeln("Opcion 3")
        break;
}